DROP TABLE CROPPRODUCTION;
DROP TABLE PRODUCTION_YEAR;
DROP TABLE GEOGRAFY;

CREATE TABLE PRODUCTION_YEAR
(
    TIME_ID         Number GENERATED BY DEFAULT on null AS IDENTITY (START WITH 1),
    PRODUCTION_YEAR Number NOT NULL,
    CONSTRAINT PK_PROD_Y PRIMARY KEY (TIME_ID)
);


CREATE TABLE GEOGRAFY
(
    GEOGRAFY_ID  Number       GENERATED BY DEFAULT on null AS IDENTITY (START WITH 2000),
    COUNTRY VARCHAR(200) NOT NULL,
    CONSTRAINT PK_GEO PRIMARY KEY (GEOGRAFY_ID)
);

CREATE TABLE CROPPRODUCTION
(
    CROP_PRODUCTION_ID Number GENERATED BY DEFAULT on null AS IDENTITY (START WITH 3000),
    TIME_ID            Number,
    GEOGRAFY_ID        Number,
    CROP_ID            Number,
    WHEAT Number,
    BARLEY Number,
    GRAIN_CORN_INCLUDING_CCM Number,
    POTATOES Number,
    SUGARBEET Number,
    RAPESEED_AND_TURNIPS Number,
    SOYBEANS Number,
    CONSTRAINT FK_CROP_PROD_GEO FOREIGN KEY (GEOGRAFY_ID) REFERENCES GEOGRAFY (GEOGRAFY_ID),
    CONSTRAINT FK_CROP_PROD_Y FOREIGN KEY (TIME_ID) REFERENCES PRODUCTION_YEAR (TIME_ID),
    CONSTRAINT PK_CROP_PROD PRIMARY KEY (CROP_PRODUCTION_ID)
);
